namespace ArithmeticOperations
{
    internal class Program
    {
        static void Main(string[] args)
        {
            int x = 9, y = 6;
            Console.WriteLine($"{x} + {y} = {x + y}");
            Console.WriteLine($"{x} - {y} = {x - y}");
            Console.WriteLine($"{x} * {y} = {x * y}");
            Console.WriteLine($"{x} / {y} = {(double)x / y}");
            Console.WriteLine($"{x} % {y} = {x % y}");
            Console.WriteLine($"{x} % {y} = {x % y}");
        }
    }
}
